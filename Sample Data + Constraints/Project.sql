
--table creation and primary key
CREATE TABLE contains (
    monstercode NUMBER(5) NOT NULL,
    dungeoncode NUMBER(5) NOT NULL
);

ALTER TABLE contains ADD CONSTRAINT contains_pk PRIMARY KEY ( monstercode,
                                                              dungeoncode );

CREATE TABLE dungeon (
    dungeoncode          NUMBER(5) NOT NULL,
    difficultylevel      NUMBER(1) NOT NULL,
    minmonstercount      INTEGER NOT NULL,
    maxmonstercount      INTEGER NOT NULL,
    refreshcooldowntimer INTEGER,
    ownerid              NUMBER(5),
    bosscode             NUMBER(5) NOT NULL,
    regioncode           NUMBER(5) NOT NULL
);

CREATE UNIQUE INDEX dungeon__idx ON
    dungeon (
        bosscode
    ASC );
    
drop index dungeon__idx;

ALTER TABLE dungeon ADD CONSTRAINT dungeon_pk PRIMARY KEY ( dungeoncode );

CREATE TABLE gem (
    gemcode NUMBER(1) NOT NULL,
    value   INTEGER NOT NULL,
    color   VARCHAR2(20) NOT NULL
);

ALTER TABLE gem ADD CONSTRAINT gem_pk PRIMARY KEY ( gemcode );

CREATE TABLE guide_helper (
    employeeid NUMBER(5) NOT NULL,
    rank       NUMBER(1) NOT NULL,
    commission INTEGER NOT NULL,
    clientid   NUMBER(5)
);

CREATE UNIQUE INDEX guide_helper__idx ON
    guide_helper (
        clientid
    ASC );

ALTER TABLE guide_helper ADD CONSTRAINT guide_helper_pk PRIMARY KEY ( employeeid );

CREATE TABLE guild (
    guildid        NUMBER(5) NOT NULL,
    name           VARCHAR2(30) NOT NULL,
    factionelement VARCHAR2(15),
    guildleaderid  NUMBER(5) NOT NULL,
    regioncode     NUMBER(5) NOT NULL
);

CREATE UNIQUE INDEX guild__idx ON
    guild (
        guildleaderid
    ASC );

ALTER TABLE guild ADD CONSTRAINT guild_pk PRIMARY KEY ( guildid );

CREATE TABLE hunter (
    hunterid      NUMBER(5) NOT NULL,
    name          VARCHAR2(30) NOT NULL,
    class         VARCHAR2(15) NOT NULL,
    rank          NUMBER(1) NOT NULL,
    datejoined    DATE NOT NULL,
    questscleared INTEGER NOT NULL,
    mainweapon    VARCHAR2(20),
    guildid       NUMBER(5)
);

ALTER TABLE hunter ADD CONSTRAINT hunter_pk PRIMARY KEY ( hunterid );

CREATE TABLE hunter_association_branch (
    branchid   NUMBER(5) NOT NULL,
    regioncode NUMBER(5) NOT NULL
);

CREATE UNIQUE INDEX hunter_association_branch__idx ON
    hunter_association_branch (
        regioncode
    ASC );

ALTER TABLE hunter_association_branch ADD CONSTRAINT hunter_association_branch_pk PRIMARY KEY ( branchid );

CREATE TABLE hunter_association_employee (
    employeeid NUMBER(5) NOT NULL,
    name       VARCHAR2(30) NOT NULL,
    salary     INTEGER NOT NULL,
    hiredate   DATE NOT NULL,
    branchid   NUMBER(5) NOT NULL,
    emptype    INTEGER NOT NULL
);

ALTER TABLE hunter_association_employee ADD CONSTRAINT hunter_association_employee_pk PRIMARY KEY ( employeeid );

CREATE TABLE monster (
    monstercode     NUMBER(5) NOT NULL,
    nametype        VARCHAR2(30) NOT NULL,
    difficultylevel NUMBER(1) NOT NULL,
    healthpoints    INTEGER NOT NULL,
    weapon          VARCHAR2(20),
    isboss          CHAR(1) NOT NULL,
    basedamage      INTEGER NOT NULL,
    monstercorecode NUMBER(1) NOT NULL
);

ALTER TABLE monster ADD CONSTRAINT monster_pk PRIMARY KEY ( monstercode );

CREATE TABLE receptionist (
    employeeid         NUMBER(5) NOT NULL,
    authorizationlevel NUMBER(1) NOT NULL
);

ALTER TABLE receptionist ADD CONSTRAINT receptionist_pk PRIMARY KEY ( employeeid );

CREATE TABLE region (
    regioncode NUMBER(5) NOT NULL,
    regionname VARCHAR2(30) NOT NULL
);

ALTER TABLE region ADD CONSTRAINT region_pk PRIMARY KEY ( regioncode );

CREATE TABLE skill_moves (
    skillmove   VARCHAR2(25) NOT NULL,
    monstercode NUMBER(5) NOT NULL
);

ALTER TABLE skill_moves ADD CONSTRAINT skill_moves_pk PRIMARY KEY ( skillmove,
                                                                    monstercode );

CREATE TABLE special_abilities (
    specialability VARCHAR2(25) NOT NULL,
    hunterid       NUMBER(5) NOT NULL
);

ALTER TABLE special_abilities ADD CONSTRAINT special_abilities_pk PRIMARY KEY ( specialability,
                                                                                hunterid );


