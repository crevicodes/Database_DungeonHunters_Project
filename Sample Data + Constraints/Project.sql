CREATE OR REPLACE TRIGGER arc_fkarc_1_receptionist BEFORE
    INSERT OR UPDATE OF employeeid ON receptionist
    FOR EACH ROW
DECLARE
    d INTEGER;
BEGIN
    SELECT
        a.emptype
    INTO d
    FROM
        hunter_association_employee a
    WHERE
        a.employeeid = :new.employeeid;

    IF ( d IS NULL OR d <> 1 ) THEN
        raise_application_error(-20223, 'FK Receptionist_HAE_FK in Table Receptionist violates Arc constraint on Table Hunter_Association_Employee - discriminator column EmpType doesn''t have value 1'
        );
    END IF;

EXCEPTION
    WHEN no_data_found THEN
        NULL;
    WHEN OTHERS THEN
        RAISE;
END;
